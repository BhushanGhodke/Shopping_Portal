package com.tcs.flipkart.product.binding;

public class ProductBinding {

private Integer id;
	
	private String name;
	
	private String brand;
	
	private String description;
	
	private Integer price;
	
	private Integer imageModelId;

	private Integer  categoryId;
	
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Integer getPrice() {
		return price;
	}

	public void setPrice(Integer price) {
		this.price = price;
	}

	

	public Integer getImageModelId() {
		return imageModelId;
	}

	public void setImageModelId(Integer imageModelId) {
		this.imageModelId = imageModelId;
	}

	public Integer getCategoryId() {
		return categoryId;
	}

	public void setCategoryId(Integer categoryId) {
		this.categoryId = categoryId;
	}

	

	 

	
	
}
